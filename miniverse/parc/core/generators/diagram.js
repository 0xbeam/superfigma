document.addEventListener('click', function(e) {
  var tab = e.target.closest('[data-tab]');
  if (!tab) return;
  var id = tab.dataset.tab;
  document.querySelectorAll('.tab').forEach(function(t) { t.classList.remove('active'); });
  document.querySelectorAll('.tab-content').forEach(function(t) { t.classList.remove('active'); });
  var panel = document.getElementById('tab-' + id);
  if (panel) panel.classList.add('active');
  tab.classList.add('active');
});

// ─── Search / filter ───
(function() {
  var input = document.getElementById('search-input');
  if (!input) return;
  input.addEventListener('input', function() {
    var q = input.value.toLowerCase().trim();
    document.querySelectorAll('.searchable').forEach(function(el) {
      if (!q) { el.style.display = ''; return; }
      var text = (el.textContent || '').toLowerCase();
      el.style.display = text.indexOf(q) !== -1 ? '' : 'none';
    });
  });
})();

// ─── Dark mode toggle ───
(function() {
  var btn = document.getElementById('dark-toggle');
  if (!btn) return;
  // Restore preference
  if (localStorage.getItem('parc-dark') === '1') document.body.classList.add('dark');
  btn.addEventListener('click', function() {
    document.body.classList.toggle('dark');
    localStorage.setItem('parc-dark', document.body.classList.contains('dark') ? '1' : '0');
  });
})();
