/** Escape user data for safe HTML embedding */
function esc(str) {
  if (str == null) return "";
  return String(str)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

export async function generateDiagram(analysis, outputPath) {
  const html = buildHTML(analysis);
  writeFileSync(outputPath, html, "utf8");
}

